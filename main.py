# 3. GRAPH GENERATOR (Asymptotic Scaling) - NON-BLOCKING
# -----------------------------------------------------------------------------
def plot_asymptotic(phase_name, protocol_instance):
    print(f"\n{'='*60}")
    print(f"Generating Asymptotic Scaling Graph for {phase_name}...")
    print(f"{'='*60}")
    batch_sizes = [1, 10, 50, 100, 250, 500]
    times = []

    # Create a temporary protocol instance for benchmarking to avoid polluting the main DB
    temp_proto = PCDLProtocol()
    temp_proto.A = protocol_instance.A  # Use same A matrix
    temp_proto.msk_W = protocol_instance.msk_W

    for n in batch_sizes:
        t_total = 0
        if phase_name == "Registration":
            for i in range(n):
                dt, _ = temp_proto.register(f"bench_{i}", "pass", track_perf=False)
                t_total += dt
        elif phase_name == "Authentication":
            # Register users first
            for i in range(n):
                temp_proto.register(f"auth_bench_{i}", "pass", track_perf=False)
            # Then authenticate
            for i in range(n):
                _, _, dt = temp_proto.authenticate(f"auth_bench_{i}", "pass", track_perf=False)
                t_total += dt
        elif phase_name == "Key Rotation":
            # Register n users, then rotate them all (time scales with n)
            for i in range(n):
                temp_proto.register(f"rot_bench_{i}", "pass", track_perf=False)
            # Now rotate all n users
            _, _, _, t_total = temp_proto.rotate(track_perf=False)

        times.append(t_total * 1000)

    # Plot
    plt.figure(figsize=(10, 5))
    color_map = {"Registration": "blue", "Authentication": "green", "Key Rotation": "red"}
    plt.plot(batch_sizes, times, marker='s', linestyle='-', linewidth=2,
             markersize=8, color=color_map.get(phase_name, 'blue'))
    plt.title(f"Asymptotic {phase_name} Performance", fontweight='bold', fontsize=14)
    plt.xlabel("Number of Users", fontsize=12)
    plt.ylabel("Total Latency (ms)", fontsize=12)
    plt.grid(True, alpha=0.3)

    # Make plot non-blocking
    plt.tight_layout()
    plt.draw()
    plt.pause(0.001)
    print(f"\n[Graph displayed. You can close it or leave it open.]\n")

