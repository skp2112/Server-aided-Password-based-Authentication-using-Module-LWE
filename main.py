# -----------------------------------------------------------------------------
# 1. OPTIMIZED NTT ENGINE
# -----------------------------------------------------------------------------
class LatticeEngine:
    def __init__(self):
        self.n = 256
        self.q = 3329
        self.zeta = 17
        self.zetas = np.array([pow(self.zeta, int('{:07b}'.format(i)[::-1], 2), self.q) for i in range(128)])

    def ntt(self, poly):
        r = poly.copy().astype(int)
        k, level_len = 1, 128
        while level_len >= 2:
            for start in range(0, 256, level_len * 2):
                z = self.zetas[k]; k += 1
                for j in range(start, start + level_len):
                    t = (z * r[j + level_len]) % self.q
                    r[j + level_len] = (r[j] - t) % self.q
                    r[j] = (r[j] + t) % self.q
            level_len //= 2
        return r % self.q

def cbd_sample(seed):
    data = hashlib.shake_128(seed.encode()).digest(64)
    poly = np.zeros(256, dtype=int)
    for i in range(256):
        bits = (data[i // 4] >> ((i % 4) * 2)) & 0x3
        poly[i] = (bits & 0x1) - ((bits >> 1) & 0x1)
    return poly % 3329

# -----------------------------------------------------------------------------
# 2. PROTOCOL LOGIC
# -----------------------------------------------------------------------------
class PCDLProtocol:
    def __init__(self):
        self.eng = LatticeEngine()
        self.q, self.l = 3329, 2
        self.limit, self.window, self.lock_duration = 5, 60, 30
        self.msk_W = "SERVER_MASTER_KEY_001"
        self.Wj = "WEBSITE_TAG_ALPHA"
        self.A = [[np.random.randint(0, self.q, 256) for _ in range(self.l)] for _ in range(self.l)]
        self.DB_W = {}
        self.DB_S = {}

        # Performance tracking
        self.perf_stats = {
            'registration': [],
            'authentication': [],
            'key_rotation': []
        }

    def register(self, un, pwd, track_perf=True):
        t0 = time.perf_counter()
        ti = hashlib.sha256((un + self.Wj).encode()).hexdigest()
        ri_poly = np.random.randint(0, self.q, 256)
        hi = cbd_sample(f"{pwd}{un}{ri_poly.tobytes()}")
        si = cbd_sample(f"{self.msk_W}{ti}{self.Wj}")
        ei = cbd_sample(f"{self.msk_W}{ti}{si.tobytes()}")
        ski_ntt = self.eng.ntt((hi + si) % self.q)
        ei_ntt = self.eng.ntt(ei)
        Bi = []
        for i in range(self.l):
            row = (ei_ntt + sum(self.A[i][j] * ski_ntt for j in range(self.l))) % self.q
            Bi.append(row)
        self.DB_W[ti] = {'un': un, 'ri_poly': ri_poly, 'Bi': Bi}
        self.DB_S[ti] = {'count': 0, 'last_fail': 0, 'lock_until': 0}
        elapsed = time.perf_counter() - t0
        if track_perf:
            self.perf_stats['registration'].append(elapsed * 1000)
        return elapsed, ti

    def authenticate(self, un, pwd_in, track_perf=True):
        t0 = time.perf_counter()
        ti = hashlib.sha256((un + self.Wj).encode()).hexdigest()
        if ti not in self.DB_W: return None, "USER_NOT_FOUND", 0
        state = self.DB_S[ti]
        curr_time = time.time()
        if curr_time < state['lock_until']:
            return None, f"LOCKED_OUT_{int(state['lock_until'] - curr_time)}s", 0

        ri_poly, Bi_stored = self.DB_W[ti]['ri_poly'], self.DB_W[ti]['Bi']
        hi_p = cbd_sample(f"{pwd_in}{un}{ri_poly.tobytes()}")
        si = cbd_sample(f"{self.msk_W}{ti}{self.Wj}")
        ei = cbd_sample(f"{self.msk_W}{ti}{si.tobytes()}")
        ski_p_ntt = self.eng.ntt((hi_p + si) % self.q)
        ei_ntt = self.eng.ntt(ei)
        Bi_calc = [(ei_ntt + sum(self.A[i][j] * ski_p_ntt for j in range(self.l))) % self.q for i in range(self.l)]

        is_valid = all(np.array_equal(Bi_calc[i], Bi_stored[i]) for i in range(self.l))
        elapsed = time.perf_counter() - t0

        if is_valid:
            self.DB_S[ti] = {'count': 0, 'last_fail': 0, 'lock_until': 0}
            if track_perf:
                self.perf_stats['authentication'].append(elapsed * 1000)
            return Bi_calc, "SUCCESS", elapsed
        else:
            if curr_time - state['last_fail'] > self.window: state['count'] = 1
            else: state['count'] += 1
            state['last_fail'] = curr_time
            if state['count'] >= self.limit: state['lock_until'] = curr_time + self.lock_duration
            return Bi_calc, f"FAIL_{state['count']}", elapsed

    def rotate(self, track_perf=True):
        """Perform actual key rotation with proper database updates"""
        t0 = time.perf_counter()
        old_msk = self.msk_W
        new_msk = "NEW_KEY_" + str(time.time())

        # Store old entries before rotation
        old_entries = {}
        for ti, data in self.DB_W.items():
            old_entries[ti] = {
                'un': data['un'],
                'Bi': [b.copy() for b in data['Bi']]
            }

        # Perform actual rotation
        for ti, data in self.DB_W.items():
            si_old = cbd_sample(f"{old_msk}{ti}{self.Wj}")
            ei_old_ntt = self.eng.ntt(cbd_sample(f"{old_msk}{ti}{si_old.tobytes()}"))
            si_new = cbd_sample(f"{new_msk}{ti}{self.Wj}")
            ei_new_ntt = self.eng.ntt(cbd_sample(f"{new_msk}{ti}{si_new.tobytes()}"))
            delta_s_ntt = self.eng.ntt((si_new - si_old) % self.q)

            # Update Bi with proper calculation
            Bi_new = []
            for i in range(self.l):
                # Bi_new = Bi_old - ei_old + delta_s + ei_new (all operations in NTT domain)
                updated = (data['Bi'][i] - ei_old_ntt + sum(self.A[i][j] * delta_s_ntt for j in range(self.l)) + ei_new_ntt) % self.q
                Bi_new.append(updated)

            data['Bi'] = Bi_new

        # Update master key
        self.msk_W = new_msk
        elapsed = time.perf_counter() - t0

        if track_perf:
            self.perf_stats['key_rotation'].append(elapsed * 1000)

        return old_msk, new_msk, old_entries, elapsed

